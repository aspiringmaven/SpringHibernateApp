package me.sumitkawatra.model;

public class PrototypeBean {
	private int val;

	public PrototypeBean() {

	}

	public int getVal() {
		return val;
	}

	public void setVal(int val) {
		this.val = val;
	}

	@Override
	public String toString() {
		return "PrototypeBean [val=" + val + "]";
	}
	
	
	
	
	

}
